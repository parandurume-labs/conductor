#!/usr/bin/env node
'use strict';

const fs = require('fs');
const path = require('path');
const { extractFrontmatter, extractField, extractMetadataField } = require('./shared');

const ROOT_DIR = path.join(__dirname, '..', '..');
const SKILLS_DIR = path.join(ROOT_DIR, 'skills');
const AGENTS_MD = path.join(ROOT_DIR, 'AGENTS.md');
const CLAUDE_MD = path.join(ROOT_DIR, 'CLAUDE.md');

function discoverSkills() {
  if (!fs.existsSync(SKILLS_DIR)) {
    return [];
  }

  const entries = fs.readdirSync(SKILLS_DIR, { withFileTypes: true });
  const skills = [];

  for (const entry of entries) {
    if (!entry.isDirectory()) continue;

    const skillFile = path.join(SKILLS_DIR, entry.name, 'SKILL.md');
    if (!fs.existsSync(skillFile)) continue;

    const content = fs.readFileSync(skillFile, 'utf-8');
    const frontmatter = extractFrontmatter(content);
    if (!frontmatter) continue;

    const name = extractField(frontmatter, 'name');
    const description = extractField(frontmatter, 'description');

    // Collect reference files
    const refsDir = path.join(SKILLS_DIR, entry.name, 'references');
    const references = [];
    if (fs.existsSync(refsDir)) {
      const refFiles = fs.readdirSync(refsDir).filter(f => f.endsWith('.md')).sort();
      for (const refFile of refFiles) {
        const refContent = fs.readFileSync(path.join(refsDir, refFile), 'utf-8');
        references.push({ name: refFile, content: refContent });
      }
    }

    const benefitsFrom = extractMetadataField(frontmatter, 'benefits-from');

    skills.push({
      dirName: entry.name,
      name: name || entry.name,
      description: description || '',
      benefitsFrom: benefitsFrom || '',
      content,
      references
    });
  }

  // Sort alphabetically by name
  skills.sort((a, b) => a.name.localeCompare(b.name));
  return skills;
}

function buildAgentsMd(skills) {
  const lines = [];
  lines.push('# AGENTS.md');
  lines.push('');
  lines.push('> Auto-generated by `npm run build`. Do not edit manually.');
  lines.push(`> Generated: ${new Date().toISOString()}`);
  lines.push(`> Skills: ${skills.length}`);
  lines.push('');
  lines.push('---');

  for (const skill of skills) {
    lines.push('');
    lines.push(`<!-- BEGIN SKILL: ${skill.name} -->`);
    lines.push('');
    lines.push(skill.content.trim());

    for (const ref of skill.references) {
      lines.push('');
      lines.push(`<!-- Reference: ${ref.name} -->`);
      lines.push('');
      lines.push(ref.content.trim());
    }

    lines.push('');
    lines.push(`<!-- END SKILL: ${skill.name} -->`);
    lines.push('');
    lines.push('---');
  }

  return lines.join('\n') + '\n';
}

function buildClaudeMd(skills) {
  const lines = [];
  lines.push('# CLAUDE.md — Skill Index');
  lines.push('');
  lines.push('> Auto-generated by `npm run build`. Do not edit manually.');
  lines.push('');
  lines.push('This file helps Claude Code discover available skills. Each skill\'s full content is in `AGENTS.md`.');
  lines.push('');
  lines.push('| Skill | Command | Description |');
  lines.push('|---|---|---|');

  for (const skill of skills) {
    const desc = skill.description.length > 120
      ? skill.description.substring(0, 117) + '...'
      : skill.description;
    lines.push(`| ${skill.name} | /${skill.name} | ${desc} |`);
  }

  // Add dependency graph if any skill has benefits-from
  const skillsWithDeps = skills.filter(s => s.benefitsFrom);
  if (skillsWithDeps.length > 0) {
    lines.push('');
    lines.push('## Skill Dependencies');
    lines.push('');
    lines.push('Skills work independently, but produce better results when chained:');
    lines.push('');
    lines.push('| Skill | Benefits From |');
    lines.push('|---|---|');
    for (const skill of skillsWithDeps) {
      lines.push(`| ${skill.name} | ${skill.benefitsFrom} |`);
    }
  }

  lines.push('');
  lines.push('## Usage');
  lines.push('');
  lines.push('Invoke any skill by typing its command (e.g., `/duru-conductor`) in your AI coding agent.');
  lines.push('');
  lines.push('## Skill Files');
  lines.push('');
  for (const skill of skills) {
    lines.push(`- \`skills/${skill.dirName}/SKILL.md\``);
  }
  lines.push('');

  return lines.join('\n') + '\n';
}

function main() {
  const skills = discoverSkills();

  if (skills.length === 0) {
    console.log('⚠️  No skills found. Nothing to build.');
    process.exit(0);
  }

  // Build AGENTS.md
  const agentsContent = buildAgentsMd(skills);
  fs.writeFileSync(AGENTS_MD, agentsContent, 'utf-8');
  const agentsLines = agentsContent.split('\n').length;

  // Build CLAUDE.md
  const claudeContent = buildClaudeMd(skills);
  fs.writeFileSync(CLAUDE_MD, claudeContent, 'utf-8');
  const claudeLines = claudeContent.split('\n').length;

  // Build per-skill AGENTS.md (for agents like Antigravity that require it)
  for (const skill of skills) {
    const skillAgentsPath = path.join(SKILLS_DIR, skill.dirName, 'AGENTS.md');
    const lines = [];
    lines.push(skill.content.trim());
    for (const ref of skill.references) {
      lines.push('');
      lines.push(ref.content.trim());
    }
    const perSkillContent = lines.join('\n') + '\n';
    fs.writeFileSync(skillAgentsPath, perSkillContent, 'utf-8');
  }

  // Report
  console.log(`Built ${skills.length} skills:`);
  for (const skill of skills) {
    const refCount = skill.references.length;
    const refNote = refCount > 0 ? ` (+${refCount} reference${refCount > 1 ? 's' : ''})` : '';
    console.log(`  - ${skill.name}${refNote}`);
  }
  console.log('');
  console.log(`→ AGENTS.md  (${agentsLines} lines)`);
  console.log(`→ CLAUDE.md  (${claudeLines} lines)`);
  console.log(`→ ${skills.length} per-skill AGENTS.md files`);
}

main();
